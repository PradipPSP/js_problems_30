function lastWordsCapitalize(sentence){
    return sentence.replace(/\b\w+\b/g, word => {
        if(word.length === 1) return word.toUpperCase();
        let lastChar = word.slice(-1).toUpperCase();
        return word.slice(0, -1).toLowerCase() + lastChar;
    })
}
